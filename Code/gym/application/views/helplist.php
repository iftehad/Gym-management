				        <p>Joining a gym might by one of the best investments you can make for your health: People with gym memberships exercise more and have better measures of heart health than those who don’t belong to a health club, new research from Iowa State University shows. 

The researchers interviewed 204 gym members and 201 people without memberships about their physical activity. They discovered that 75 percent of people who belonged to a gym met the federal guidelines for 150 minutes of moderate aerobic activity (or 75 minutes of vigorous aerobic activity) per week and strength training at least two days a week. Only 18 percent of non gym-goers hit both goals. 

The activity boost translated to actual health benefits, too. Gym goers were 40 percent less likely to be obese and 50 percent less likely to have an elevated resting heart rate—a measure that has been shown to increase your risk of heart-related death—than those without memberships. Their waist circumferences measured about 1.5 inches smaller, which is important, since more fat around the middle is especially dangerous for your heart. (Here’s how you can lose belly fat in with just two exercises.)

What’s more, people with gym memberships also performed higher on tests of cardiorespiratory fitness, which measures heart strength, lung function, blood circulation, and muscle mass. 

The health benefits were even greater when people maintained their membership for over a year..</p>
